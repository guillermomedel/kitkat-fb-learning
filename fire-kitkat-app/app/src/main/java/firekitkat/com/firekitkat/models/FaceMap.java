package firekitkat.com.firekitkat.models;

public class FaceMap {
    private double leftEyeDistance;
    private double rightEyeDistance;
    private double  leftMouthDistance;
    private double rightMouthDistance;

    public double getLeftEyeDistance() {
        return leftEyeDistance;
    }

    public void setLeftEyeDistance(double leftEyeDistance) {
        this.leftEyeDistance = leftEyeDistance;
    }

    public double getRightEyeDistance() {
        return rightEyeDistance;
    }

    public void setRightEyeDistance(double rightEyeDistance) {
        this.rightEyeDistance = rightEyeDistance;
    }

    public double getLeftMouthDistance() {
        return leftMouthDistance;
    }

    public void setLeftMouthDistance(double leftMouthDistance) {
        this.leftMouthDistance = leftMouthDistance;
    }

    public double getRightMouthDistance() {
        return rightMouthDistance;
    }

    public void setRightMouthDistance(double rightMouthDistance) {
        this.rightMouthDistance = rightMouthDistance;
    }
}
